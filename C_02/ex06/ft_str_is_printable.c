#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	i;
	int	t;

	i = 0;
	t = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 32 || str[i] > 126)
		{
			t = 0;
			return (t);
		}
		i++;
	}
	return (t);
}
