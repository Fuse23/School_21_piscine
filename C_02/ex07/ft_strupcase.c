#include <unistd.h>

char	*ft_strupcase(char *str)
{
	char	*buf;

	buf = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
			*str -= 32;
		str++;
	}
	return (buf);
}
