#include<unistd.h>

void	ft_putnbr(int nb)
{
	char	a[11];
	int		i;

	i = 0;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb == 0)
		write(1, "0", 1);
	while (nb > 0)
	{
		a[i] = nb % 10 + '0';
		nb /= 10;
		i++;
	}
	while (i >= 0)
	{
		write(1, &a[i], 1);
		i--;
	}
}
