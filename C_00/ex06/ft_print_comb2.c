#include<unistd.h>

void	ft_print_comb2(void)
{
	int		in[2];
	char	out[6];

	out[2] = ' ';
	in[0] = 0;
	while (in[0] <= 98)
	{
		in[1] = in[0];
		while (in[1] <= 99)
		{
			out[1] = in[0] % 10 + '0';
			out[0] = in[0] / 10 + '0';
			out[4] = in[1] % 10 + '0';
			out[3] = in[1] / 10 + '0';
			if (out[0] <= out[3])
			{
				write(1, out, 5);
				if (in[0] != 98)
					write(1, ", ", 2);
			}
			in[1]++;
		}
		in[0]++;
	}
}
